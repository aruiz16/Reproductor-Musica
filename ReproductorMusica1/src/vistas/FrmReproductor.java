    private void txtYearKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtYearKeyTyped
        // TODO add your handling code here:
        
        //textAutoCompleter.addItem("item de prueba");
       
        int numeroCaracteres = 4;
        if (txtYear.getText().length() >= numeroCaracteres) {
            evt.consume();
            JOptionPane.showMessageDialog(this, "Sólo 4 caracteres");
        }
        if (!txtYear.getText().matches("[0-9]+")) {
            JOptionPane.showMessageDialog(null, "Solo números enteros");
            return;
        }

    }//GEN-LAST:event_txtYearKeyTyped

