    private void btnAbrirListaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAbrirListaActionPerformed
        // TODO add your handling code here:
        lista = new ListaCanciones();
        List<Cancion> canciones;
        Cancion cancion;
        CancionDOM cancionDOM = new CancionDOM();
        String pathFile = "";
        JFileChooser jfc = new JFileChooser();
        int op;
        op = jfc.showOpenDialog(this);
        if (op == JFileChooser.APPROVE_OPTION) {
            pathFile = jfc.getSelectedFile().getAbsolutePath();
            canciones = cancionDOM.getAll(pathFile);
            for (int i = 0; i < canciones.size(); i++) {
                cancion = canciones.get(i);
                lista.insertarOrden(new NodoD(cancion));
            }
        }
    }//GEN-LAST:event_btnAbrirListaActionPerformed

